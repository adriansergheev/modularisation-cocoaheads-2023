 # Modularization with SPM
 
 ***
 ## What is Modularisation?
 * Modularity refers to organising code into Swift modules. The term "module" specifically means SPM modules.
 ***
 ## Two Types of Modularity: Small impact and big impact
 * Small Impact: Extract common code (models, helpers etc) into separate modules, limited benefits 
 * Big Impact: "Feature" modularity, bundle entire features into single modules, more challenging but quite powerful.
